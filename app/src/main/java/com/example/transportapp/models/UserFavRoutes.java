package com.example.transportapp.models;

public class UserFavRoutes {

    String id;
    String name_direction;
    String direction;

    public UserFavRoutes() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName_direction() {
        return name_direction;
    }

    public void setName_direction(String name_direction) {
        this.name_direction = name_direction;
    }

    public String getDirection() {
        return direction;
    }

    public void setDirection(String direction) {
        this.direction = direction;
    }
}
